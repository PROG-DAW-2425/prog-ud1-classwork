package classwork.java;

public class Activiitat18 {

    static final int KBYTES_1MB = 1024;

    public static void main(String[] args) {

        int valorA = 40000;
        int valorB = 36000;

        System.out.println(valorA + " MB equivalen a " + valorA * KBYTES_1MB + " KB");
        System.out.println(valorB + " MB equivalen a " + valorB * KBYTES_1MB + " KB");

    }
}
